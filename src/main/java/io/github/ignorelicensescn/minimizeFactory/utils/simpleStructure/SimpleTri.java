package io.github.ignorelicensescn.minimizeFactory.utils.simpleStructure;

public class SimpleTri <A,B,C>{
    public final A first;
    public final B second;
    public final C third;
    public SimpleTri(A a, B b,C c){
        this.first = a;
        this.second = b;
        this.third = c;
    }
}
